package com.javaFundamentals;

public class TestDataTypes {

	public static void main(String[] args) {
		int a=0745;
		int b=0Xabc;
		int c=0xfaf; 
		System.out.println("Octa decimal :"+a);// range 0-8
		//7*8^2+4*8^1+5**^0
		System.out.println("Hexa Decimal  :"+b);// range 0-9 to a-f
		//10*16^2+11*16^1+12^16^0
		System.out.println("Hexa Decimal  :"+c);

	}

}
