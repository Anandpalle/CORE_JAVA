package com.patterns;

public class ABCpattern {
	void main() {
		for( char i=65;i<=68;i++) {
			for(char j=65;j<=i;j++) {
			System.out.print(i);
			}
			System.out.println();
		}
		
		
	
	
	for( char i=97;i<=100;i++) {
		for(char j=97;j<=i;j++) {
		System.out.print(i);
		}
		System.out.println();
	}
	
	
}
}
